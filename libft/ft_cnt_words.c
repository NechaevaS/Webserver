#include "libft.h"

int	ft_cnt_words(char const *s, char c)
{
	int cnt;
	int i;

	i = 0;
	cnt = 0;
	while (s[i] != '\0')
	{
		if (s[i] != c && (s[i - 1] == c || i == 0))
			cnt++;
		i++;
	}
	return (cnt);
}
